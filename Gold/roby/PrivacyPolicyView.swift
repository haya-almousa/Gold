internal import SwiftUI

struct PrivacyPolicyView: View {
    @Environment(\.dismiss) private var dismiss

    var body: some View {
        NavigationStack {
            ScrollView(showsIndicators: false) {
                VStack(alignment: .leading, spacing: 24) {

                    header

                    section(title: "١. مقدمة") {
                        """
                        مرحبًا بك في تطبيق "تبرة". نحن نلتزم بحماية خصوصيتك وبياناتك \
                        الشخصية وفقًا لنظام حماية البيانات الشخصية الصادر عن الهيئة السعودية \
                        للبيانات والذكاء الاصطناعي (سدايا) ولائحة حوكمة البيانات المعتمدة في \
                        المملكة العربية السعودية. توضّح هذه السياسة كيفية جمع بياناتك \
                        واستخدامها وتخزينها وحمايتها.
                        """
                    }

                    section(title: "٢. البيانات التي نجمعها") {
                        """
                        أ) بيانات الحساب (عند تسجيل الدخول بحساب Apple):
                        • الاسم الكامل
                        • البريد الإلكتروني
                        • معرّف المستخدم (User ID)

                        ب) بيانات القطع الذهبية (تُدخلها بنفسك):
                        • اسم القطعة، الوزن بالجرام، العيار، الحالة
                        • سعر الشراء، تاريخ التملّك
                        • صور القطع الذهبية (من الكاميرا أو معرض الصور)

                        ج) بيانات الاشتراك:
                        • حالة الاشتراك تتم إدارتها بالكامل عبر Apple ولا نحتفظ بأي \
                        بيانات دفع.
                        """
                    }

                    section(title: "٣. الغرض من جمع البيانات") {
                        """
                        نستخدم بياناتك للأغراض التالية فقط:
                        • إنشاء حسابك وتمكينك من تسجيل الدخول
                        • حفظ بيانات قطعك الذهبية وعرضها لك
                        • حساب زكاة الذهب بناءً على بياناتك
                        • عرض أسعار الذهب المحدّثة
                        • مقارنة أسعار القطع الذهبية
                        • إدارة اشتراكك في خدمة تبرة بلس

                        لا نستخدم بياناتك لأي أغراض تسويقية أو إعلانية، ولا نشاركها مع \
                        أي طرف ثالث.
                        """
                    }

                    section(title: "٤. الأساس النظامي للمعالجة") {
                        """
                        وفقًا للمادة (٦) من نظام حماية البيانات الشخصية، نعالج بياناتك بناءً على:
                        • موافقتك الصريحة عند تسجيل الدخول بحساب Apple
                        • موافقتك عند إدخال بيانات قطعك الذهبية طوعيًا
                        • موافقتك عند السماح بالوصول إلى الكاميرا أو معرض الصور

                        يمكنك سحب موافقتك في أي وقت عن طريق حذف بياناتك أو تسجيل \
                        الخروج من التطبيق.
                        """
                    }

                    section(title: "٥. تخزين البيانات وحمايتها") {
                        """
                        • جميع بياناتك الشخصية تُخزَّن محليًا على جهازك فقط باستخدام \
                        تقنيات Apple الآمنة (SwiftData و UserDefaults).
                        • لا تُرسَل بياناتك الشخصية إلى أي خادم خارجي.
                        • الاتصال الوحيد بالإنترنت هو لجلب أسعار الذهب المحدّثة من \
                        مزوّد أسعار خارجي، ولا يتضمّن هذا الطلب أي بيانات شخصية.
                        • بيانات الاشتراك تُدار بالكامل عبر أنظمة Apple الآمنة.
                        """
                    }

                    section(title: "٦. نقل البيانات خارج المملكة") {
                        """
                        وفقًا للمادة (٢٩) من نظام حماية البيانات الشخصية، نؤكّد أن:
                        • بياناتك الشخصية تُخزَّن محليًا على جهازك داخل المملكة.
                        • لا ننقل أي بيانات شخصية إلى خوادم خارج المملكة العربية السعودية.
                        • خدمة أسعار الذهب تستقبل فقط طلبات عامة بدون أي بيانات شخصية.
                        """
                    }

                    section(title: "٧. حقوقك كصاحب بيانات") {
                        """
                        وفقًا للمادة (١٤) من نظام حماية البيانات الشخصية، لديك الحقوق التالية:
                        • حق الوصول: الاطلاع على بياناتك المخزّنة في التطبيق
                        • حق التصحيح: تعديل بيانات قطعك الذهبية في أي وقت
                        • حق الحذف: حذف أي قطعة ذهبية أو تسجيل الخروج لإزالة بيانات حسابك
                        • حق سحب الموافقة: التوقف عن استخدام التطبيق وحذفه في أي وقت
                        • حق الاعتراض: التواصل معنا للاعتراض على أي معالجة لبياناتك

                        لممارسة أي من هذه الحقوق، يمكنك التواصل معنا عبر البريد الإلكتروني \
                        المذكور أدناه.
                        """
                    }

                    section(title: "٨. حذف البيانات") {
                        """
                        • يمكنك حذف بيانات قطعك الذهبية من داخل التطبيق مباشرةً.
                        • تسجيل الخروج يزيل بيانات حسابك (الاسم، البريد، المعرّف) من الجهاز.
                        • حذف التطبيق يزيل جميع البيانات المخزّنة نهائيًا لأنها محفوظة \
                        محليًا فقط.
                        """
                    }

                    section(title: "٩. الوصول إلى الكاميرا ومعرض الصور") {
                        """
                        نطلب إذنك للوصول إلى الكاميرا أو معرض الصور فقط عند رغبتك في \
                        إضافة صورة لقطعة ذهبية. الصور تُخزَّن محليًا على جهازك فقط ولا \
                        تُرسَل لأي جهة خارجية.
                        """
                    }

                    section(title: "١٠. حماية بيانات الأطفال") {
                        """
                        التطبيق غير موجّه للأطفال دون سن ١٨ عامًا. لا نجمع عمدًا بيانات \
                        من أطفال. إذا علمنا بجمع بيانات طفل سنحذفها فورًا.
                        """
                    }

                    section(title: "١١. التعديلات على السياسة") {
                        """
                        قد نحدّث هذه السياسة من وقت لآخر. سنُعلمك بأي تغييرات جوهرية \
                        عبر تحديث التطبيق. استمرارك في استخدام التطبيق بعد التحديث يعني \
                        موافقتك على السياسة المعدّلة.
                        """
                    }

                    section(title: "١٢. التواصل معنا") {
                        """
                        لأي استفسارات أو طلبات تتعلّق ببياناتك الشخصية أو هذه السياسة، \
                        يُرجى التواصل معنا عبر:
                        البريد الإلكتروني: Tabrahapp@gmail.com
                        """
                    }

                    Text("آخر تحديث: يونيو ٢٠٢٦")
                        .font(.appCaption())
                        .foregroundColor(Color("Grey"))
                        .frame(maxWidth: .infinity, alignment: .center)
                        .padding(.bottom, 40)
                }
                .padding(.horizontal, 20)
            }
            .background(Color("background").ignoresSafeArea())
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                ToolbarItem(placement: .topBarTrailing) {
                    Button { dismiss() } label: {
                        Image(systemName: "xmark")
                            .font(.appSubheadline(.semibold))
                            .foregroundColor(Color("maincolor"))
                    }
                }
            }
        }
        .environment(\.layoutDirection, .rightToLeft)
    }

    private var header: some View {
        VStack(alignment: .leading, spacing: 8) {
            Text("سياسة الخصوصية")
                .font(.appTitle2(.bold))
                .foregroundColor(.black)

            Text("تطبيق تبرة — لإدارة وتتبّع الذهب")
                .font(.appSubheadline())
                .foregroundColor(Color("Grey"))
        }
        .frame(maxWidth: .infinity, alignment: .leading)
        .padding(.top, 16)
    }

    private func section(title: String, content: () -> String) -> some View {
        VStack(alignment: .leading, spacing: 10) {
            Text(title)
                .font(.appCallout(.bold))
                .foregroundColor(Color("maincolor"))

            Text(content())
                .font(.appSubheadline())
                .foregroundColor(.black.opacity(0.85))
                .multilineTextAlignment(.leading)
                .lineSpacing(6)
                .fixedSize(horizontal: false, vertical: true)
        }
        .frame(maxWidth: .infinity, alignment: .leading)
        .padding(16)
        .background(Color("Lightest blue"))
        .cornerRadius(14)
        .overlay(
            RoundedRectangle(cornerRadius: 14)
                .stroke(Color("maincolor").opacity(0.1), lineWidth: 0.5)
        )
    }
}

